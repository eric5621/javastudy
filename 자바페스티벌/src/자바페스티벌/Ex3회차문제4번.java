package 자바페스티벌;

import java.util.Scanner;

public class Ex3회차문제4번 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		int sum = 1;
		System.out.print("n 입력 : ");
		int n = sc.nextInt();
		for(int i = 0; i < n; i++){
			sum = sum + i ;
			System.out.print(sum+" ");
		}
		
		
		
		
		
		
		
		
		
	}

}
