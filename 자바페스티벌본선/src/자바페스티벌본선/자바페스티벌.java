package 자바페스티벌본선;


import java.util.Scanner;

public class 자바페스티벌 {

	public static void main(String[] args) {
		int num = 1;

		int[][] array = new int[5][5];
		
		for (int i = 0; i < array.length; i++) {
			for (int j = 0; j < array.length; j++) {
				array[i][j] = num++;
			}
			
		}
		
		
		
	}
	
	
	
}